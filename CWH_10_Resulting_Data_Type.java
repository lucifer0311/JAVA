package com.company;

public class CWH_10_Resulting_Data_Type {
    public static void main(String[] args) {
        byte x = 5;
        int y = 6;
        short z = 8;
        int a = y + z;
        float b = 6.54f + x;
        System.out.println(b);

        // Increment And Decrement Opeartors

        int i = 56;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);

        char ch = 'B';
        System.out.println(--ch);  // Decrement

    }
}
